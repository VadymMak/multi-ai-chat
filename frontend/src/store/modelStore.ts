import { create } from "zustand";
import { persist, createJSONStorage } from "zustand/middleware";

// âœ… Supported AI model providers
export type ModelProvider = "openai" | "anthropic" | "grok" | "boost" | "all";

// âœ… Role structure
export interface MemoryRole {
  id: number;
  name: string;
}

// âœ… Zustand store state
interface ModelStore {
  provider: ModelProvider;
  role: MemoryRole | null;
  setProvider: (provider: ModelProvider) => void;
  setRole: (role: MemoryRole | null) => void;
}

// âœ… Zustand store with built-in persistence
export const useModelStore = create<ModelStore>()(
  persist(
    (set) => ({
      provider: "openai",
      role: null,

      setProvider: (provider) => {
        set({ provider });
      },

      setRole: (role) => {
        set({ role });
      },
    }),
    {
      name: "model-store",
      version: 1,
      storage: createJSONStorage(() => sessionStorage), // âœ… Matches chat/memory/project store
      partialize: (state) => ({
        provider: state.provider, // ðŸ§  Keep provider if needed across reloads
        role: state.role, // ðŸ§  Discard after session if needed
      }),
    }
  )
);
